package kr.co.kosmo.mvc.vo;
/*
 * NO	NUMBER
ID	VARCHAR2(10 BYTE)
PW	VARCHAR2(10 BYTE)
NAME	VARCHAR2(34 BYTE)
AGE	NUMBER(5,0)
GENDER	VARCHAR2(5 BYTE)
MDATE	DATE
 */
public class MemberDTO {
	private int no;
	private String id, pw, name, age, gender, mdate;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getMdate() {
		return mdate;
	}
	public void setMdate(String mdate) {
		this.mdate = mdate;
	}
	
	
}
